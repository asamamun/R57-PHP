<h1>In PHP 7, a new feature, spaceship operator has been introduced. It is used to compare two expressions. It returns -1, 0 or 1 when first expression is respectively less than, equal to, or greater than second expression.</h1>
<?php
// echo 7<=>6 . "<br>";
echo (77<=>6) . "<br>";
echo (66<=>66) . "<br>";
echo (5<=>9) . "<br>";
echo (-1<=>-1) . "<br>";